package pojo;

public class Ball {
	private int size;
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public char getColour() {
		return colour;
	}
	public void setColour(char colour) {
		this.colour = colour;
	}
	public char getMake() {
		return make;
	}
	public void setMake(char make) {
		this.make = make;
	}
	private char colour;
	private char make;
	
}
